package com.publit.publit_io.constant;

/**
 * The type Players contants.
 */
public class PlayersContants {

    private PlayersContants() {
        // not to be instantiated in public
    }

    /**
     * Autoplay option for player, valid 0 (off), 1 (on), 2 (on mouseover).
     * Default 0.
     */
    public static final String AUTO_PLAY = "auto_play";

    /**
     * Skin for player. Valid values are: blue, pink, green, grey, orange and inverted.
     * Default grey.
     */
    public static final String SKIN = "skin";

    /**
     * Unique alphanumeric name (id) of adtag you want to use with this player.
     */
    public static final String ADTAG_ID = "adtag_id";

}
