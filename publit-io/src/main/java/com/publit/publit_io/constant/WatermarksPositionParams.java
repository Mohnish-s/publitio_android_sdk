package com.publit.publit_io.constant;

/**
 * Watermarks position params.
 */
public class WatermarksPositionParams {

    private WatermarksPositionParams() {
        // not to be instantiated in public
    }

    /**
     * Set watermark position to CENTER.
     */
    public static final String CENTER = "center";

    /**
     * Set watermark position to TOP.
     */
    public static final String TOP = "top";

    /**
     * Set watermark position to LEFT.
     */
    public static final String LEFT = "left";

    /**
     * Set watermark position to RIGHT.
     */
    public static final String RIGHT = "right";

    /**
     * Set watermark position to BOTTOM.
     */
    public static final String BOTTOM = "bottom";

    /**
     * Set watermark position to TOP_LEFT.
     */
    public static final String TOP_LEFT = "top-left";

    /**
     * Set watermark position to TOP_RIGHT.
     */
    public static final String TOP_RIGHT = "top-right";

    /**
     * Set watermark position to BOTTOM_LEFT.
     */
    public static final String BOTTOM_LEFT = "bottom-left";

    /**
     * Set watermark position to BOTTOM_RIGHT.
     */
    public static final String BOTTOM_RIGHT = "bottom-right";

}
