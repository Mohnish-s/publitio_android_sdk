package com.publit.publit_io.constant;

/**
 * Version image type params.
 */
public class VersionImageParams {

    private VersionImageParams() {
        // not to be instantiated in public
    }

    /**
     * Set version output format to JPG.
     */
    public static final String JPG = "jpg";

    /**
     * Set version output format to JPEG.
     */
    public static final String JPEG = "jpeg";

    /**
     * Set version output format to PNG.
     */
    public static final String PNG = "png";

    /**
     * Set version output format to GIF.
     */
    public static final String GIF = "gif";

    /**
     * Set version output format to BMP.
     */
    public static final String BMP = "bmp";

    /**
     * Set version output format to WEBP.
     */
    public static final String WEBP = "webp";

    /**
     * Set version output format to MP4.
     */
    public static final String MP4 = "mp4";

    /**
     * Set version output format to WEBM.
     */
    public static final String WEBM = "webm";

    /**
     * Set version output format to OGV.
     */
    public static final String OGV = "ogv";

}
