package com.publit.publit_io.constant;

/**
 * Files resolutions params.
 */
public class FilesResolutions {

    public FilesResolutions() {
        // not to be instantiated in public
    }

    /**
     * Set video resolution to 640 X 480.
     */
    public static final String RES_640X480 = "640:480";

    /**
     * Set video resolution to 960 X 540.
     */
    public static final String RES_720X480 = "960:540";

    /**
     * Set video resolution to 1280 X 720. It is a default resolution.
     */
    public static final String RES_1280X720 = "1280:720";

    /**
     * Set video resolution to 1920 X 1080.
     */
    public static final String RES_1920X1080 = "1920:1080";
}
